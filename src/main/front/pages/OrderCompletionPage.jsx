import React, { useState, useEffect } from 'react';
import { useParams, useNavigate } from 'react-router-dom';
import Header from '../components/Header';
import Footer from '../components/Footer';
import styles from '../styles/OrderCompletionPage.module.css';
import { getOrderById, updateOrder } from '../api/api';

export default function OrderCompletionPage() {
    const { id } = useParams();
    const navigate = useNavigate();
    const [order, setOrder] = useState(null);
    const [loading, setLoading] = useState(true);
    const [actionLoading, setActionLoading] = useState(false);

    useEffect(() => {
        const fetchOrder = async () => {
            try {
                if (!id) {
                    setLoading(false);
                    return;
                }

                const response = await getOrderById(id);
                if (response.success) {
                    setOrder(response.data);
                } else {
                    console.error('–û—à–∏–±–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ –∑–∞–∫–∞–∑–∞:', response.error);
                }
            } catch (error) {
                console.error('–û—à–∏–±–∫–∞ –ø—Ä–∏ –∑–∞–≥—Ä—É–∑–∫–µ –∑–∞–∫–∞–∑–∞:', error);
            } finally {
                setLoading(false);
            }
        };

        fetchOrder();
    }, [id]);

    const handleMarkComplete = async () => {
        if (!order) return;

        setActionLoading(true);
        try {
            const updateData = {
                ...order,
                status: 'completed',
                completionDate: new Date().toISOString().split('T')[0] // –¢–µ–∫—É—â–∞—è –¥–∞—Ç–∞
            };

            const response = await updateOrder(order.id, updateData);
            if (response.success) {
                alert(`–ó–∞–∫–∞–∑ ‚Ññ${order.id} –æ—Ç–º–µ—á–µ–Ω –∫–∞–∫ –≤—ã–ø–æ–ª–Ω–µ–Ω–Ω—ã–π`);
                navigate('/profile');
            } else {
                alert('–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ—Ç–º–µ—Ç–∫–µ –∑–∞–∫–∞–∑–∞ –∫–∞–∫ –≤—ã–ø–æ–ª–Ω–µ–Ω–Ω–æ–≥–æ: ' + response.error);
            }
        } catch (error) {
            console.error('–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ—Ç–º–µ—Ç–∫–µ –∑–∞–∫–∞–∑–∞:', error);
            alert('–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ –æ—Ç–º–µ—Ç–∫–µ –∑–∞–∫–∞–∑–∞');
        } finally {
            setActionLoading(false);
        }
    };

    const handleMarkExpired = async () => {
        if (!order) return;

        if (!window.confirm('–í—ã —É–≤–µ—Ä–µ–Ω—ã, —á—Ç–æ —Ö–æ—Ç–∏—Ç–µ –æ—Ç–º–µ—Ç–∏—Ç—å –∑–∞–∫–∞–∑ –∫–∞–∫ –ø—Ä–æ—Å—Ä–æ—á–µ–Ω–Ω—ã–π?')) {
            return;
        }

        setActionLoading(true);
        try {
            const updateData = {
                ...order,
                status: 'expired'
            };

            const response = await updateOrder(order.id, updateData);
            if (response.success) {
                alert(`–ó–∞–∫–∞–∑ ‚Ññ${order.id} –æ—Ç–º–µ—á–µ–Ω –∫–∞–∫ –ø—Ä–æ—Å—Ä–æ—á–µ–Ω–Ω—ã–π`);
                navigate('/profile');
            } else {
                alert('–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ—Ç–º–µ—Ç–∫–µ –∑–∞–∫–∞–∑–∞ –∫–∞–∫ –ø—Ä–æ—Å—Ä–æ—á–µ–Ω–Ω–æ–≥–æ: ' + response.error);
            }
        } catch (error) {
            console.error('–û—à–∏–±–∫–∞ –ø—Ä–∏ –æ—Ç–º–µ—Ç–∫–µ –∑–∞–∫–∞–∑–∞:', error);
            alert('–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ –æ—Ç–º–µ—Ç–∫–µ –∑–∞–∫–∞–∑–∞');
        } finally {
            setActionLoading(false);
        }
    };

    if (loading) {
        return (
            <div className={styles.page}>
                <Header isAuthenticated={true} />
                <main className={styles.main}>
                    <div className={styles.container}>
                        <div className={styles.loading}>–ó–∞–≥—Ä—É–∑–∫–∞ –¥–∞–Ω–Ω—ã—Ö –∑–∞–∫–∞–∑–∞...</div>
                    </div>
                </main>
                <Footer />
            </div>
        );
    }

    if (!order) {
        return (
            <div className={styles.page}>
                <Header isAuthenticated={true} />
                <main className={styles.main}>
                    <div className={styles.container}>
                        <h1 className={styles.title}>–ó–∞–∫–∞–∑ –Ω–µ –Ω–∞–π–¥–µ–Ω</h1>
                        <p className={styles.error}>–ó–∞–∫–∞–∑ —Å ID {id} –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –∏–ª–∏ –±—ã–ª —É–¥–∞–ª–µ–Ω.</p>
                        <button
                            className={styles.backButton}
                            onClick={() => navigate('/profile')}
                        >
                            –í–µ—Ä–Ω—É—Ç—å—Å—è –≤ –ø—Ä–æ—Ñ–∏–ª—å
                        </button>
                    </div>
                </main>
                <Footer />
            </div>
        );
    }

    return (
        <div className={styles.page}>
            <Header isAuthenticated={true} />
            <main className={styles.main}>
                <div className={styles.container}>
                    <h1 className={styles.title}>–ó–∞–∫–∞–∑ ‚Ññ{order.id}</h1>

                    <div className={styles.content}>
                        <div className={styles.imageWrapper}>
                            <div className={styles.placeholderImage}>
                                üìã
                            </div>
                        </div>

                        <div className={styles.info}>
                            <h2 className={styles.subtitle}>{order.name || '–ù–∞–∑–≤–∞–Ω–∏–µ –Ω–µ —É–∫–∞–∑–∞–Ω–æ'}</h2>
                            <p className={styles.description}>
                                {order.quest || '–û–ø–∏—Å–∞–Ω–∏–µ –∑–∞–∫–∞–∑–∞ –æ—Ç—Å—É—Ç—Å—Ç–≤—É–µ—Ç'}
                            </p>
                            {order.manager && (
                                <p>
                                    <strong>–û—Ç–≤–µ—Ç—Å—Ç–≤–µ–Ω–Ω—ã–π –º–µ–Ω–µ–¥–∂–µ—Ä:</strong>{' '}
                                    {order.manager.user ?
                                        `${order.manager.user.firstname} ${order.manager.user.lastname}` :
                                        '–ù–µ —É–∫–∞–∑–∞–Ω'
                                    }
                                </p>
                            )}
                            {order.customer && (
                                <p>
                                    <strong>–ó–∞–∫–∞–∑—á–∏–∫:</strong> {order.customer}
                                </p>
                            )}
                            {/* –î–æ–±–∞–≤—å—Ç–µ –¥—Ä—É–≥–∏–µ –ø–æ–ª—è –∑–∞–∫–∞–∑–∞ –ø–æ –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ—Å—Ç–∏ */}
                        </div>
                    </div>

                    <div className={styles.actions}>
                        <button
                            onClick={handleMarkComplete}
                            className={`${styles.button} ${styles.green}`}
                            disabled={actionLoading}
                        >
                            {actionLoading ? '–û–±—Ä–∞–±–æ—Ç–∫–∞...' : '–í—ã–ø–æ–ª–Ω–µ–Ω'}
                        </button>
                        <button
                            onClick={handleMarkExpired}
                            className={`${styles.button} ${styles.red}`}
                            disabled={actionLoading}
                        >
                            {actionLoading ? '–û–±—Ä–∞–±–æ—Ç–∫–∞...' : '–ü—Ä–æ—Å—Ä–æ—á–µ–Ω'}
                        </button>
                    </div>
                </div>
            </main>
            <Footer />
        </div>
    );
}